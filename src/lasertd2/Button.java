package lasertd2;

/**
 *
 * @author Xels
 */
public class Button {

   public int xPos;
   public int yPos;
   public int width;
   public int height;
   public int type;

   public Button(int x, int y, int width, int height, int type){
      this.xPos = x;
      this.yPos = y;
      this.width = width;
      this.height = height;
      this.type = type;
   }
}
